package com.chinarewards.elt.model.common;

public class Unit {

	private String name;
	private String code;
	private double rateToCNY;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getRateToCNY() {
		return rateToCNY;
	}

	public void setRateToCNY(double rateToCNY) {
		this.rateToCNY = rateToCNY;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
}
